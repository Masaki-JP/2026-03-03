const params = new URLSearchParams(window.location.search);
const item = params.get("item") || "";
const detail = swiftuiDetails[item];
const titleEl = document.querySelector("[data-detail-title]");
const badgeEl = document.querySelector("[data-detail-badge]");
const summaryEl = document.querySelector("[data-detail-summary]");
const descEl = document.querySelector("[data-detail-description]");
const pointsEl = document.querySelector("[data-detail-points]");
const sampleEl = document.querySelector("[data-detail-sample]");
if (!detail) {
  titleEl.textContent = "項目が見つかりません";
  badgeEl.textContent = "Not Found";
  summaryEl.textContent = "一覧ページからもう一度選択してください。";
  descEl.textContent = "URLパラメータ item が無効です。";
  sampleEl.textContent = "// swiftui.html から再度アクセス";
} else {
  document.title = `${detail.title} の詳細 | Code EJ`;
  titleEl.textContent = detail.title;
  badgeEl.textContent = detail.category;
  summaryEl.textContent = detail.summary;
  descEl.textContent = detail.description;
  pointsEl.innerHTML = detail.points.map((point) => `<li>${point}</li>`).join("");
  sampleEl.textContent = detail.sample;
}